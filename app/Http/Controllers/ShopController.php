     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            "promotion_status"=>"required|min:2|max:10",
            "shop_category"=>"required",
        ]);

        $shop = Shop::find($id);
        $shop->promotion_status = $request->promotion_status;
        $shop->shop_category = $request->shop_category;
        $shop->update();
        return redirect()->route('shop.index')->with("toast","Shop info is updated");
    }

